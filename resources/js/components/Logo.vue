        name: 'Logo',
        components: {},
        props: {
            color: {
                type: String,
                default: null,
            },
            iconOnly: {
                type: Boolean,
                default: false,
            },
            iconColor: {
                type: String,
                default: 'black',
            },
            textColor: {
                type: String,
                default: 'black',
            },
            size: {
                type: String,
                default: null,
            },
        },
        data() {
            return {};
        },
        computed: {
            svgClass() {
                const color = this.color || this.iconColor;
                return ['fill-current', `text-${color}`, 'h-full', 'w-auto'];
            },
            textClass() {
                const color = this.getTextColor();
                let size = null;

                switch (this.size) {
                    case 'large':
                    case 'lg':
                        size = 'text-xl tracking-wide';
                        break;
                    case 'xlarge':
                    case 'xl':
                        size = 'text-3xl tracking-wider';
                        break;
                    default:
                        size = 'text-lg';
                        break;
                }

                return ['ml-2', size, `text-${color}`];
            },
            heightClass() {
                switch (this.size) {
                    case 'large':
                    case 'lg':
                        return 'h-10';
                    case 'xlarge':
                    case 'xl':
                        return 'h-12';
                    default:
                        return 'h-8';
                }
            },
        },
        watch: {},
        created() {},
        mounted() {},
        methods: {
            getTextColor() {
                let initialColor = this.color || this.textColor;

                if (initialColor === 'white' || initialColor === 'black') {
                    return initialColor;
                }

                let parts = initialColor.split('-');

                if (parts[1] === '900') {
                    return parts.concat('-');
                }

                let int = parseInt(parts[1].charAt(0));
                int++;
                int = int * 100;
                int = int + '';

                return `${parts[0]}-${int}`;
            },
        },
    };
